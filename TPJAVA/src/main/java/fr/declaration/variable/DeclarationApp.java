package fr.declaration.variable;

public class DeclarationApp {

	public static void main(String[] args) {
		
		byte a = 1;
		short b =2;
		int c = 3;
		long d = 5642168519681655L;
		float e = 1.12f;
		double f = 1598665.23d;
		char g = 'g';
		boolean h = true;
		String i = "i";
		
		
		System.out.println("as: "+a + "\n"+"b: "+b + "\n"+"c: "+c + "\n"+"d: "+d + "\n"+"e: "+e + "\n"+"f: "+f + "\n"+"g: "+g + "\n"+"h: "+h + "\n"+ "i: " + i);


	}

}
